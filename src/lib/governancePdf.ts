// ---- Table drawing helpers ------------------------------------------------

function drawTableHeader(
  doc: jsPDF,
  headers: string[],
  colWidths: number[],
  startX: number,
  y: number,
): number {
  y = addPageBreakIfNeeded(doc, y, 10);
  const rowH = 8;

  // Grey header background
  doc.setFillColor(243, 244, 246); // gray-100
  const totalW = colWidths.reduce((a, b) => a + b, 0);
  doc.rect(startX, y - 5, totalW, rowH, "F");

  doc.setFont("helvetica", "bold");
  doc.setFontSize(8);
  doc.setTextColor(...TEXT_COLOR);

  let x = startX + 2;
  for (let i = 0; i < headers.length; i++) {
    doc.text(headers[i], x, y);
    x += colWidths[i];
  }

  // Bottom border
  doc.setDrawColor(209, 213, 219); // gray-300
  doc.setLineWidth(0.3);
  doc.line(startX, y + 3, startX + totalW, y + 3);

  return y + rowH;
}

function drawToolRows(
  doc: jsPDF,
  tools: Array<{
    name: string;
    purpose: string;
    dataClassification: string;
    departments: string;
  }>,
  colWidths: number[],
  startX: number,
  y: number,
): number {
  doc.setFont("helvetica", "normal");
  doc.setFontSize(8);
  doc.setTextColor(...TEXT_COLOR);

  const totalW = colWidths.reduce((a, b) => a + b, 0);

  for (const tool of tools) {
    y = addPageBreakIfNeeded(doc, y, 8);
    const values = [
      tool.name || "",
      tool.purpose || "",
      tool.dataClassification || "",
      tool.departments || "",
    ];

    let x = startX + 2;
    for (let i = 0; i < values.length; i++) {
      // Truncate if too long for the cell
      const maxChars = Math.floor(colWidths[i] / 1.8);
      const text =
        values[i].length > maxChars
          ? values[i].slice(0, maxChars - 1) + "\u2026"
          : values[i];
      doc.text(text, x, y);
      x += colWidths[i];
    }

    // Row border
    doc.setDrawColor(229, 231, 235); // gray-200
    doc.setLineWidth(0.2);
    doc.line(startX, y + 3, startX + totalW, y + 3);
    y += 7;
  }

  return y;
}

function riskColor(category: string): [number, number, number] {
  const lower = (category || "").toLowerCase();
  if (lower === "minimal" || lower === "limited") return GREEN;
  if (lower === "high") return AMBER;
  if (lower === "unacceptable") return RED;
  return MUTED_COLOR; // unassessed or unknown
}

function drawVendorRows(
  doc: jsPDF,
  vendors: Array<{ vendor_name: string; risk_category: string }>,
  colWidths: number[],
  startX: number,
  y: number,
): number {
  doc.setFontSize(8);
  const totalW = colWidths.reduce((a, b) => a + b, 0);

  for (const vendor of vendors) {
    y = addPageBreakIfNeeded(doc, y, 8);

    // Vendor name
    doc.setFont("helvetica", "normal");
    doc.setTextColor(...TEXT_COLOR);
    doc.text(vendor.vendor_name || "Unknown", startX + 2, y);

    // Risk category (colour-coded)
    const risk = vendor.risk_category || "unassessed";
    doc.setTextColor(...riskColor(risk));
    doc.setFont("helvetica", "bold");
    doc.text(risk.charAt(0).toUpperCase() + risk.slice(1), startX + colWidths[0] + 2, y);

    // Row border
    doc.setDrawColor(229, 231, 235);
    doc.setLineWidth(0.2);
    doc.line(startX, y + 3, startX + totalW, y + 3);
    y += 7;
  }

  return y;
}
